SET SERVEROUTPUT ON
DECLARE
CURSOR CURESAL IS
SELECT EMPNAME,SAL FROM EMPSAL
WHERE SAL>30000;
ENAME EMPSAL.EMPNAME%TYPE;
ESAL EMPSAL.SAL%TYPE;
BEGIN
OPEN CURESAL;
LOOP
FETCH CURESAL INTO ENAME,ESAL;
EXIT WHEN CURESAL%NOTFOUND;
DBMS_OUTPUT.PUT_LINE(ENAME||' DRAWS SALARY '||ESAL);
END LOOP;
CLOSE CURESAL;
END;
/