SET SERVEROUTPUT ON
DECLARE
	ENO  NUMBER(9,2);
	ENAME VARCHAR(25);
	SAL  NUMBER(9,2);
	HRA  NUMBER(9,2);
	DA  NUMBER(9,2);
	GROSS  NUMBER(9,2);
	PF  NUMBER(9,2);
	NETSAL  NUMBER(9,2);
BEGIN
	ENO:=&ENO;
	ENAME:='&ENAME';
	SAL:=&SAL;
	HRA:=SAL*0.5;
	DA:=SAL*0.2;
	PF:=SAL*0.12;
	GROSS:=SAL+HRA+DA;
	NETSAL:=GROSS-PF;
INSERT INTO EMPSAL VALUES(ENO,ENAME,SAL,HRA,DA,PF,GROSS,NETSAL);
END;
/
