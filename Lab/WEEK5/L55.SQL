SET SERVEROUTPUT ON
DECLARE
CURSOR CURHI(PRJID VARCHAR2) IS
SELECT ENAME,SAL,DEPTNO,MAX(SAL) AS SALARY 
FROM EMP
GROUP BY PRJ_ID
WHERE PRJ_ID='PRJID';
EP EMP.ENAME%TYPE;
ESAL EMP.SAL%TYPE;
DEPT EMP.DEPTNO%TYPE;
BEGIN
OPEN CURHI('&PRJID');
LOOP
FETCH CURHI INTO EP,ESAL,DEPT;
EXIT WHEN CURHI%NOTFOUND;
DBMS_OUTPUT.PUT_LINE(EP||' '||ESAL||' '||DEPT);
END LOOP;
CLOSE CURHI;
END;
/